//5.Função para Calcular Dias de Vida 
function calcularDiasDeVida(idadeEmAnos) {
    let diasDeVida = idadeEmAnos * 365;
    console.log(`Uma pessoa de ${idadeEmAnos} anos viveu aproximadamente ${diasDeVida} dias `);
} 
console.log('-----------------------------------------------------')
calcularDiasDeVida(23);
console.log('-----------------------------------------------------')